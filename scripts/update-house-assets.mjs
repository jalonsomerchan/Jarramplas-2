import { readdir, readFile, writeFile } from "node:fs/promises";
import { join } from "node:path";

const housesDir = "assets/generated/houses";
const outputJs = "game/house-assets.js";
const outputManifest = join(housesDir, "manifest.json");
const pwaAssets = "pwa-assets.js";

function houseNumber(name) {
  return Number(/^house(\d+)\.png$/i.exec(name)?.[1] || 0);
}

function readPngSize(buffer) {
  const signature = buffer.subarray(0, 8).toString("hex");
  if (signature !== "89504e470d0a1a0a") throw new Error("No es un PNG valido");
  return {
    w: buffer.readUInt32BE(16),
    h: buffer.readUInt32BE(20),
  };
}

function toJsString(value) {
  return JSON.stringify(value);
}

const filenames = (await readdir(housesDir))
  .filter((name) => /^house\d+\.png$/i.test(name))
  .sort((a, b) => houseNumber(a) - houseNumber(b));

if (!filenames.length) {
  throw new Error(`No se encontraron casas con formato houseN.png en ${housesDir}`);
}

const houseAssets = await Promise.all(filenames.map(async (name) => {
  const src = `${housesDir}/${name}`;
  const { w, h } = readPngSize(await readFile(src));
  const number = houseNumber(name);
  return { src, label: `Casa ${number}`, w, h };
}));

const js = `// AUTO-GENERATED by npm run update:houses. Do not edit by hand.
export const houseAssets = [
${houseAssets.map((house) => (
  `  { src: ${toJsString(house.src)}, label: ${toJsString(house.label)}, w: ${house.w}, h: ${house.h} },`
)).join("\n")}
];
`;

await writeFile(outputJs, js);
await writeFile(`${outputManifest}`, `${JSON.stringify(houseAssets, null, 2)}\n`);

const pwa = await readFile(pwaAssets, "utf8");
const pwaList = houseAssets.map((house) => `    ${toJsString(house.src)},`).join("\n");
const updatedPwa = pwa.replace(
  /([ \t]*\/\/ HOUSE_ASSETS_START\n)[\s\S]*?([ \t]*\/\/ HOUSE_ASSETS_END)/,
  `$1${pwaList}\n$2`,
);
if (updatedPwa === pwa) {
  throw new Error(`No se encontro el bloque HOUSE_ASSETS en ${pwaAssets}`);
}
await writeFile(pwaAssets, updatedPwa);

console.log(`Actualizadas ${houseAssets.length} casas desde ${housesDir}`);
