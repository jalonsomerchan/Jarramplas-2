import { readdir, readFile, writeFile } from "node:fs/promises";
import { join } from "node:path";

const objectsDir = "assets/generated/objects";
const outputJs = "game/object-assets.js";
const outputManifest = join(objectsDir, "manifest.json");
const pwaAssets = "pwa-assets.js";

function objectNumber(name) {
  return Number(/^object(\d+)\.png$/i.exec(name)?.[1] || 0);
}

function readPngSize(buffer) {
  const signature = buffer.subarray(0, 8).toString("hex");
  if (signature !== "89504e470d0a1a0a") throw new Error("No es un PNG valido");
  return {
    w: buffer.readUInt32BE(16),
    h: buffer.readUInt32BE(20),
  };
}

function toJsString(value) {
  return JSON.stringify(value);
}

const filenames = (await readdir(objectsDir))
  .filter((name) => /^object\d+\.png$/i.test(name))
  .sort((a, b) => objectNumber(a) - objectNumber(b));

if (!filenames.length) {
  throw new Error(`No se encontraron objetos con formato objectN.png en ${objectsDir}`);
}

const objectAssets = await Promise.all(filenames.map(async (name) => {
  const src = `${objectsDir}/${name}`;
  const { w, h } = readPngSize(await readFile(src));
  const number = objectNumber(name);
  return { src, label: `Objeto ${number}`, w, h };
}));

const js = `// AUTO-GENERATED by npm run update:objects. Do not edit by hand.
export const objectAssets = [
${objectAssets.map((object) => (
  `  { src: ${toJsString(object.src)}, label: ${toJsString(object.label)}, w: ${object.w}, h: ${object.h} },`
)).join("\n")}
];
`;

await writeFile(outputJs, js);
await writeFile(outputManifest, `${JSON.stringify(objectAssets, null, 2)}\n`);

const pwa = await readFile(pwaAssets, "utf8");
const pwaList = objectAssets.map((object) => `    ${toJsString(object.src)},`).join("\n");
const updatedPwa = pwa.replace(
  /([ \t]*\/\/ OBJECT_ASSETS_START\n)[\s\S]*?([ \t]*\/\/ OBJECT_ASSETS_END)/,
  `$1${pwaList}\n$2`,
);
if (updatedPwa === pwa) {
  throw new Error(`No se encontro el bloque OBJECT_ASSETS en ${pwaAssets}`);
}
await writeFile(pwaAssets, updatedPwa);

console.log(`Actualizados ${objectAssets.length} objetos desde ${objectsDir}`);
