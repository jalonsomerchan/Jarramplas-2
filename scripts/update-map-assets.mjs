import { readdir, readFile, writeFile } from "node:fs/promises";
import { join, parse } from "node:path";

const mapsDir = "maps";
const outputJs = "game/map-assets.js";
const outputLayouts = "game/scenario-layouts.js";
const outputManifest = join(mapsDir, "manifest.json");
const pwaAssets = "pwa-assets.js";

function slugify(value) {
  return value
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "")
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-|-$/g, "") || "mapa";
}

function toJsString(value) {
  return JSON.stringify(value);
}

function mapMeta(map) {
  if (map.meta) return map.meta;
  const houses = map.houses?.length || 0;
  const objects = map.objects?.length || 0;
  return `${houses} edificios · ${objects} objetos`;
}

function normalizeMap(source, filename) {
  const id = slugify(source.id || parse(filename).name);
  return {
    id,
    name: source.name || id,
    source: `${mapsDir}/${filename}`,
    world: source.world || { w: 3200, h: 2400 },
    ground: source.ground || ["#a98258", "#96704c", "#b08a62"],
    paths: source.paths || "#615d58",
    pathRects: source.pathRects || [],
    plazas: source.plazas || [],
    spawn: source.spawn || { player: [1200, 1700], jarramplas: [1760, 930], target: [1450, 980] },
    houses: source.houses || [],
    objects: source.objects || [],
    bystanders: source.bystanders || [],
    meta: mapMeta(source),
  };
}

function toLayout(map) {
  const layout = {
    ground: map.ground,
    paths: map.paths,
    pathRects: map.pathRects,
    plazas: map.plazas,
    objects: map.objects,
    spawn: map.spawn,
    houses: map.houses,
  };
  if (map.bystanders.length) layout.bystanders = map.bystanders;
  return layout;
}

const filenames = (await readdir(mapsDir))
  .filter((name) => /\.json$/i.test(name))
  .filter((name) => !/-example\.json$/i.test(name) && !/editor-example\.json$/i.test(name))
  .sort((a, b) => a.localeCompare(b, "es"));

if (!filenames.length) {
  throw new Error(`No se encontraron mapas creados en ${mapsDir}`);
}

const maps = await Promise.all(filenames.map(async (name) => (
  normalizeMap(JSON.parse(await readFile(join(mapsDir, name), "utf8")), name)
)));

const mapAssets = maps.map((map) => ({
  id: map.id,
  name: map.name,
  source: map.source,
  meta: map.meta,
  ground: map.ground,
  houses: map.houses.length,
  objects: map.objects.length,
}));

const js = `// AUTO-GENERATED by npm run update:maps. Do not edit by hand.
export const mapAssets = [
${mapAssets.map((map) => (
  `  { id: ${toJsString(map.id)}, name: ${toJsString(map.name)}, source: ${toJsString(map.source)}, meta: ${toJsString(map.meta)}, ground: ${JSON.stringify(map.ground)}, houses: ${map.houses}, objects: ${map.objects} },`
)).join("\n")}
];
`;

const layoutsJs = `// AUTO-GENERATED by npm run update:maps. Do not edit by hand.
export const scenarioLayouts = ${JSON.stringify(Object.fromEntries(maps.map((map) => [map.id, toLayout(map)])), null, 2)};
`;

await writeFile(outputJs, js);
await writeFile(outputLayouts, layoutsJs);
await writeFile(outputManifest, `${JSON.stringify(mapAssets, null, 2)}\n`);

const pwa = await readFile(pwaAssets, "utf8");
const pwaList = maps.map((map) => `    ${toJsString(map.source)},`).join("\n");
const pwaBlockPattern = /([ \t]*\/\/ MAP_ASSETS_START\n)[\s\S]*?([ \t]*\/\/ MAP_ASSETS_END)/;
if (!pwaBlockPattern.test(pwa)) {
  throw new Error(`No se encontro el bloque MAP_ASSETS en ${pwaAssets}`);
}
const updatedPwa = pwa.replace(pwaBlockPattern, `$1${pwaList}\n$2`);
await writeFile(pwaAssets, updatedPwa);

console.log(`Actualizados ${maps.length} mapas creados desde ${mapsDir}`);
